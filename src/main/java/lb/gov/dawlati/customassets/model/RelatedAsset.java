package lb.gov.dawlati.customassets.model;

public class RelatedAsset {

	private String relatedAssetTitle;
	private String relatedAssetDescription;
	public String getRelatedAssetTitle() {
		return relatedAssetTitle;
	}
	public void setRelatedAssetTitle(String relatedAssetTitle) {
		this.relatedAssetTitle = relatedAssetTitle;
	}
	public String getRelatedAssetDescription() {
		return relatedAssetDescription;
	}
	public void setRelatedAssetDescription(String relatedAssetDescription) {
		this.relatedAssetDescription = relatedAssetDescription;
	}
	
	
}
